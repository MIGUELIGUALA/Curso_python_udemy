vocal = input ("Inserte una vocal ")

if vocal.lower () == "a":
    print(" Elejiste la vocal a")
else:
    if vocal.lower() == "e":
        print ("Elejiste la vocal e")
    else:
        if vocal.lower() == "i":
           print("Elejiste la vocal i")
        else:
            if vocal.lower() == "o":
                print ("Elejiste la vocal o")
            else:
                if vocal.lower() == "u":
                    print ("Elejiste la vocal u")
                else:
                    print("ESO NO ES UNA VOCAL")


